package cn.edu.bnuz.seawas.vo.wechat;

import cn.edu.bnuz.seawas.entity.Photo;

/**
 * 微信端显示图片Vo
 * Created by sjk on 2017-09-08.
 */
public class PhotoVo {

    private Photo photo;    //照片信息

    private Boolean isLike;     //是否已点赞

    public Photo getPhoto() {
        return photo;
    }

    public void setPhoto(Photo photo) {
        this.photo = photo;
    }

    public Boolean getLike() {
        return isLike;
    }

    public void setLike(Boolean like) {
        isLike = like;
    }

    @Override
    public String toString() {
        return "PhotoVo{" +
                "photo=" + photo +
                ", isLike=" + isLike +
                '}';
    }
}
